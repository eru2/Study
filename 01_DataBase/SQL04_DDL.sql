CREATE VIEW VW_COACH_PROFESSOR
AS (SELECT STUDENT_NAME, DEPARTMENT_NAME, NVL(PROFESSOR_NAME, 0) AS "COACH_PROFESSOR_NAME"
    FROM TB_STUDENT S
    JOIN TB_DEPARTMENT D ON (S.DEPARTMENT_NO = D.DEPARTMENT_NO)
    LEFT JOIN TB_PROFESSOR P ON (S.DEPARTMENT_NO = D.DEPARTMENT_NO)
    );

    
