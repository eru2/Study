CREATE OR REPLACE VIEW VW_DEPARTMENT_STUDENT
AS (SELECT DEPARTMENT_NAME, COUNT(*) AS "STUDENT_COUNT"
    FROM TB_STUDENT
    LEFT JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
    GROUP BY DEPARTMENT_NAME
    ORDER BY DEPARTMENT_NAME);

